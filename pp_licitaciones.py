import streamlit as st
import pandas as pd
import matplotlib.pyplot as plt
import seaborn as sns

# =============================
# CONFIGURACI√ìN GENERAL Y ESTILO
# =============================
st.set_page_config(page_title="An√°lisis Licitaciones", layout="wide")

# Estilo gr√°fico con tonos lila y palo rosa
PALETA_PASTEL = ["#d8bfd8", "#dda0dd", "#e6a9ec", "#e5bcd1", "#f7d6e0"]  # tonos pastel
plt.style.use("seaborn-v0_8-muted")  # estilo suave

# =============================
# CARGA DE DATOS
# =============================
@st.cache_data
def cargar_datos():
    return pd.read_parquet("data_licitaciones_2023_2024_reducido.parquet")

DF = cargar_datos()

# =============================
# SIDEBAR Y FILTROS
# =============================
st.sidebar.image("logo_ust.png", width=180)  # Logo UST
st.sidebar.title("üìä Navegaci√≥n")

seccion = st.sidebar.radio("Ir a secci√≥n:", [
    "Introducci√≥n", "Gasto P√∫blico", "Competitividad", "Eficiencia", "Transparencia",
    "Municipios", "Comparaci√≥n 2023 vs 2024", "Conclusiones"
])

st.sidebar.markdown("---")
selected_year = st.sidebar.selectbox("üìÖ Selecciona el a√±o", sorted(DF["A√±o"].unique()))
selected_rubro = st.sidebar.selectbox("üè∑Ô∏è Filtrar por Rubro (opcional)", ["Todos"] + sorted(DF["RubroN1"].dropna().unique()))
selected_muni = st.sidebar.selectbox("üèõÔ∏è Filtrar por Municipio (opcional)", ["Todos"] + sorted(DF["Institucion"].dropna().unique()))

df = DF.copy()
df = df[df["A√±o"] == selected_year]
if selected_rubro != "Todos":
    df = df[df["RubroN1"] == selected_rubro]
if selected_muni != "Todos":
    df = df[df["Institucion"] == selected_muni]

# =============================
# SECCI√ìN: INTRODUCCI√ìN
# =============================
if seccion == "Introducci√≥n":
    st.image("logo_ust.png", width=200)

    st.title("üìò An√°lisis de Licitaciones Municipales 2023‚Äì2024")
    st.markdown("""
    Bienvenido al panel interactivo de an√°lisis de licitaciones p√∫blicas realizadas por municipalidades de Chile entre 2023 y 2024.

    üîç **Objetivo**: Evaluar la eficiencia, transparencia y competitividad del proceso de contrataci√≥n p√∫blica.

    üß† **¬øPor qu√© es importante?**  
    Porque una licitaci√≥n transparente y eficiente permite un mejor uso de los recursos p√∫blicos y una mayor participaci√≥n de proveedores, incluyendo PYMES.

    üìå Este proyecto forma parte del curso *Business Intelligence* de la Universidad Santo Tom√°s.
    """)
    st.divider()

# =============================
# SECCI√ìN: GASTO P√öBLICO
# =============================
elif seccion == "Gasto P√∫blico":
    st.header("üí∏ Objetivo 1: Evaluar el gasto p√∫blico")

    st.subheader("üè∑Ô∏è Top rubros por monto estimado")
    top_rubros = df.groupby("RubroN1")["MontoEstimadoLicitacion"].sum().sort_values(ascending=False).head(10)
    fig1, ax1 = plt.subplots()
    top_rubros.plot(kind="bar", ax=ax1, color=PALETA_PASTEL[0])
    ax1.set_ylabel("Monto Estimado")
    ax1.set_title("Top 10 Rubros")
    st.pyplot(fig1)
    st.caption("Rubros con mayor gasto p√∫blico estimado, destacando sectores como salud, infraestructura y servicios generales.")

    st.divider()

    st.subheader("üí∞ Distribuci√≥n de financiamiento")
    top_fin = df["FuenteFinanciamiento"].fillna("Desconocido").value_counts().head(10)
    otros = df["FuenteFinanciamiento"].fillna("Desconocido").value_counts()[10:].sum()
    top_fin["Otros"] = otros
    fig2, ax2 = plt.subplots(figsize=(6, 6))
    wedges, texts, autotexts = ax2.pie(
        top_fin, labels=None, autopct='%1.1f%%', startangle=90,
        pctdistance=1.25, labeldistance=1.4,
        colors=PALETA_PASTEL
    )
    ax2.set_title("Fuente de Financiamiento")
    ax2.legend(top_fin.index, loc="center left", bbox_to_anchor=(1, 0.5))
    st.pyplot(fig2)
    st.caption("La mayor√≠a del financiamiento proviene de fondos municipales (99.8%), lo que limita la escala de los proyectos.")

# =============================
# SECCI√ìN: COMPETITIVIDAD
# =============================
elif seccion == "Competitividad":
    st.header("üìà Objetivo 2: Competitividad del mercado")

    st.subheader("üë• Distribuci√≥n de oferentes por licitaci√≥n")
    oferentes = df.groupby("NroLicitacion")["Proveedor"].nunique()
    fig3, ax3 = plt.subplots()
    sns.histplot(oferentes, bins=30, ax=ax3, color=PALETA_PASTEL[1])
    ax3.set_title("N√∫mero de oferentes por licitaci√≥n")
    st.pyplot(fig3)
    st.caption("Casi el 20% de licitaciones tienen un solo oferente, lo cual puede indicar baja competencia.")

    st.divider()

    st.subheader("üè¢ Adjudicaciones por tama√±o de proveedor")
    df_adjudicada = df[df["ResultadoOferta"] == "Adjudicada"]
    tamano = df_adjudicada["TamanoProveedor"].value_counts(normalize=True) * 100
    if tamano.empty:
        st.warning("No hay datos de adjudicaciones disponibles para los filtros seleccionados.")
    else:
        fig4, ax4 = plt.subplots()
        tamano.plot(kind="barh", ax=ax4, color=PALETA_PASTEL[2])
        ax4.set_title("% Adjudicado por Tama√±o de Proveedor")
        st.pyplot(fig4)
        st.caption("Las grandes empresas concentran el 56% de las adjudicaciones. Las PYMES siguen en desventaja.")

# =============================
# SECCI√ìN: EFICIENCIA
# =============================
elif seccion == "Eficiencia":
    st.header("‚è±Ô∏è Objetivo 3: Eficiencia del proceso")

    df["FechaPublicacion"] = pd.to_datetime(df["FechaPublicacion"], errors="coerce")
    df["FechaAdjudicacion"] = pd.to_datetime(df["FechaAdjudicacion"], errors="coerce")
    df["Plazo"] = (df["FechaAdjudicacion"] - df["FechaPublicacion"]).dt.days

    fig5, ax5 = plt.subplots()
    sns.histplot(df["Plazo"].dropna(), bins=30, ax=ax5, color=PALETA_PASTEL[3])
    ax5.set_title("D√≠as entre publicaci√≥n y adjudicaci√≥n")
    st.pyplot(fig5)
    st.caption("El plazo promedio es de 39 a 45 d√≠as. Las licitaciones multietapa demoran un 70% m√°s que las simples.")

# =============================
# SECCI√ìN: TRANSPARENCIA
# =============================
elif seccion == "Transparencia":
    st.header("üîé Objetivo 4: Transparencia")

    st.subheader("üìÑ Tipo de licitaci√≥n")
    fig6, ax6 = plt.subplots()
    df["TipoLicitacion"].value_counts().plot(kind="bar", ax=ax6, color=PALETA_PASTEL[4])
    ax6.set_title("Distribuci√≥n de tipos de licitaci√≥n")
    st.pyplot(fig6)
    st.caption("99.95% de las licitaciones son p√∫blicas, lo que refleja transparencia formal, pero no sustantiva.")

    st.divider()

    st.subheader("üì¢ Publicaci√≥n de ofertas t√©cnicas")
    fig7, ax7 = plt.subplots()
    values = df["PublicidadOfertasTecnicas"].value_counts()
    wedges, texts, autotexts = ax7.pie(
        values, labels=None, autopct="%1.1f%%", startangle=90,
        pctdistance=1.25, labeldistance=1.4,
        colors=PALETA_PASTEL
    )
    ax7.legend(values.index, loc="center left", bbox_to_anchor=(1, 0.5))
    st.pyplot(fig7)
    st.caption("Solo el 37% de licitaciones publican su evaluaci√≥n t√©cnica, un punto d√©bil en la rendici√≥n de cuentas.")

# =============================
# SECCI√ìN: MUNICIPIOS
# =============================
elif seccion == "Municipios":
    st.header("üèôÔ∏è An√°lisis por Municipio")
    st.subheader("üèÜ Top 10 Municipios por Monto Estimado")
    top_muni = df.groupby("Institucion")["MontoEstimadoLicitacion"].sum().sort_values(ascending=False).head(10)
    fig_muni, ax_muni = plt.subplots()
    top_muni.plot(kind="barh", ax=ax_muni, color=PALETA_PASTEL[0])
    ax_muni.set_title("Top 10 Instituciones por Monto Total Estimado")
    ax_muni.set_xlabel("Monto Estimado")
    st.pyplot(fig_muni)
    st.caption("Municipios como La Cisterna y Concepci√≥n concentran el mayor volumen de gasto estimado.")

# =============================
# SECCI√ìN: COMPARACI√ìN ANUAL
# =============================
elif seccion == "Comparaci√≥n 2023 vs 2024":
    st.header("üìä Comparaci√≥n entre A√±os: 2023 vs 2024")

    df_comp = DF.copy()
    df_comp["FechaPublicacion"] = pd.to_datetime(df_comp["FechaPublicacion"], errors="coerce")
    df_comp["FechaAdjudicacion"] = pd.to_datetime(df_comp["FechaAdjudicacion"], errors="coerce")
    df_comp["Plazo"] = (df_comp["FechaAdjudicacion"] - df_comp["FechaPublicacion"]).dt.days
    df_comp = df_comp[df_comp["Plazo"].notna() & (df_comp["Plazo"] >= 0)]

    resumen = df_comp.groupby("A√±o").agg({
        "MontoEstimadoLicitacion": "sum",
        "NroLicitacion": "nunique",
        "Proveedor": "nunique",
        "Plazo": "mean"
    }).rename(columns={
        "MontoEstimadoLicitacion": "Total Monto Estimado (CLP)",
        "NroLicitacion": "Licitaciones √önicas",
        "Proveedor": "Proveedores √önicos",
        "Plazo": "Plazo Promedio (d√≠as)"
    })

    resumen["Plazo Promedio (d√≠as)"] = resumen["Plazo Promedio (d√≠as)"].round(2)
    resumen["Total Monto Estimado (CLP)"] = resumen["Total Monto Estimado (CLP)"].astype(int)

    st.dataframe(resumen.style.format({
        "Total Monto Estimado (CLP)": "{:,} CLP",
        "Plazo Promedio (d√≠as)": "{:.2f} d√≠as"
    }))
    st.caption("Se observa una mejora de eficiencia y aumento en diversidad de proveedores en 2024.")

# =============================
# SECCI√ìN: CONCLUSIONES
# =============================
